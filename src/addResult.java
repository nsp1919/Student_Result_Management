    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        String maths=jTextField2.getText();
        String Physics=jTextField3.getText();
        String Chemistry=jTextField4.getText();
        String clang=jTextField5.getText();
        String English=jTextField6.getText();
        String adminID=jLabel7.getText();
        if (maths.isEmpty() || Physics.isEmpty() || Chemistry.isEmpty() || rollNo.isEmpty() ||clang.isEmpty()||English.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all the details");
        }
        if ((Integer.parseInt(maths)<100) && (Integer.parseInt(maths)>0) && 
                (Integer.parseInt(clang)<100) && (Integer.parseInt(clang)>0)&&
                (Integer.parseInt(Chemistry)<100) && (Integer.parseInt(Chemistry)>0) &&
                        (Integer.parseInt(English)<100) && (Integer.parseInt(English)>0) &&
                        (Integer.parseInt(Physics)<100) && (Integer.parseInt(Physics)>0)){
            try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Sai@1234");
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("select * from student where rollno='"+rollNo+"'");
        if(rs.next()){
            st.executeUpdate("insert into result(rollno,physics,Maths,chemistry,clang,english,adminId) values('"+rollNo+"','"+Physics+"','"+maths+"','"+Chemistry+"','"+clang+"','"+English+"','"+adminID+"')");
            JOptionPane.showMessageDialog(null, "Result added succesfully");
            setVisible(false); 
            new addResult().setVisible(true);
        }
        else {
        JOptionPane.showMessageDialog(null,"Rollno not registered");
                }
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e.toString());
        }
            
        }
        
        else {
            try{
        JOptionPane.showMessageDialog(null, "Marks should be in between 0 to 100");
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString());
                }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

