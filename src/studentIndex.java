    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno=jTextField5.getText();
        try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Sai@1234");
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
        
        if (rs.next()){
            jTextField1.setText(rs.getString(2));
            jTextField4.setText(rs.getString(3));
            jTextField6.setText(rs.getString(4));
            jTextField3.setText(rs.getString(5));
            jTextField2.setText(rs.getString(6));
            jTextField10.setText(rs.getString(9));
            jTextField14.setText(rs.getString(8));
            jTextField13.setText(rs.getString(10));
            jTextField12.setText(rs.getString(11));
            jTextField11.setText(rs.getString(12));
            
            int s1=Integer.parseInt(jTextField14.getText());
            int s2=Integer.parseInt(jTextField10.getText());
            int s3=Integer.parseInt(jTextField13.getText());
            int s4=Integer.parseInt(jTextField12.getText());
            int s5=Integer.parseInt(jTextField11.getText());
            String totalmarks=String.valueOf(s1+s2+s3+s4+s5);
            jTextField8.setText(totalmarks);
        }
        
       
        }
        
        catch(Exception e){
        JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

